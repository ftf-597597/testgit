import smtplib   #封装smtp协议
import email.mime.text   #处理正文中的数据
import email.mime.multipart   #封装的是邮件的格式

sender = 'futf597597@163.com'  #定义一个发送者
reser = ['1129414282@qq.com','1423493990@qq.com']    #接收者
server = 'smtp.163.com'

#授权码
passwd = 'ftf597'
#创建一个空邮件
message = email.mime.multipart.MIMEMultipart()
#添加发件人、收件人、主题
message['From'] = sender
message['To'] = ','.join(server)
message['Subject'] = 'python_learn'
aa = """台湾是中国的
"""
cc = email.mime.text.MIMEText(aa)
#将正文添加到邮件里
message.attach(cc)

# #定义发送的附件的文件名，文件可以是任何格式的文件
att1 = email.mime.text.MIMEText(open(r'a.txt', 'rb').read(), 'base64', 'utf-8')
# 附件携带的字段和值
att1["Content-Type"] = 'application/octet-stream'
# 附件携带的字段和值  这里的filename可以任意写，写什么名字，邮件中显示什么名字
att1["Content-Disposition"] = 'attachment; filename="a.txt"'
# 将附件添加到邮件里
message.attach(att1)

#第二个附件
att2 = email.mime.text.MIMEText(open(r'312.txt', 'rb').read(), 'base64', 'utf-8')
# 附件携带的字段和值
att2["Content-Type"] = 'application/octet-stream'
# 附件携带的字段和值  这里的filename可以任意写，写什么名字，邮件中显示什么名字
att2["Content-Disposition"] = 'attachment; filename="312.txt"'
# 将附件添加到邮件里
message.attach(att2)

#定义发送邮件的服务器和端口号
smtp123 = smtplib.SMTP_SSL(server,465)  #465是端口号
#登陆服务器
smtp123.login(sender,passwd)
#发送邮件
smtp123.sendmail(sender,reser,message.as_string())  #.as_string() 需要用这个函数去处理
#断开连接
smtp123.close()
